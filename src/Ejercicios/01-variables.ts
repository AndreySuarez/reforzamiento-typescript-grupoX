// Declaramos una constante para el nombre
const nombre_estudiante: string = "Alexis";

// Declaramos una variable con let para la edad 
let edad: number = 20;

// Actualizamos el valor de edad
edad = 21;

// Imprimimos los valores en consola
console.log("Nombre del estudiante:", nombre_estudiante);
console.log("Edad del estudiante:", edad);

/*
  Diferencias entre const y let:

  - const: se usa para declarar variables cuyo valor no debe cambiar, en este caso, el nombre del estudiante no cambia.

  - let: se usa para declarar variables que pueden modificar, la edad se puede actualizar a un nuevo valor.
*/